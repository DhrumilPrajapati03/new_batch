#Type Casting (conversion)
a = int(input("enter the value1: ")) #explicit type casting
b = int(input("enter the value2: "))

c = a + b #implicit type casting
print("addition of a and b is", c)
print(type(c))

d = a - b #implicit type casting
print("subtraction of a and b is", d)

e = a / b #implicit type casting
print("Division of a and b is", e)

f = a * b #implicit type casting
print("Multiplication of a and b is", f)
